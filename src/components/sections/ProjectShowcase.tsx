/* ─────────────────────────────────────────────
   MOBILE SHOWCASE
───────────────────────────────────────────── */
function MobileShowcase() {
  const [activeIndex, setActiveIndex] = useState(0);
  const [direction, setDirection] = useState(1);
  const touchStartX = useRef(0);
  const touchStartY = useRef(0);

  const project = PROJECTS[activeIndex];

  const goTo = useCallback(
    (next: number) => {
      if (next < 0 || next >= N) return;
      setDirection(next > activeIndex ? 1 : -1);
      setActiveIndex(next);
    },
    [activeIndex],
  );

  const goPrev = useCallback(() => goTo(activeIndex - 1), [goTo, activeIndex]);
  const goNext = useCallback(() => goTo(activeIndex + 1), [goTo, activeIndex]);

  // Touch swipe
  const onTouchStart = useCallback((e: React.TouchEvent) => {
    touchStartX.current = e.touches[0].clientX;
    touchStartY.current = e.touches[0].clientY;
  }, []);

  const onTouchEnd = useCallback(
    (e: React.TouchEvent) => {
      const dx = touchStartX.current - e.changedTouches[0].clientX;
      const dy = Math.abs(touchStartY.current - e.changedTouches[0].clientY);
      if (Math.abs(dx) < 40 || dy > Math.abs(dx)) return;
      if (dx > 0) goNext();
      else goPrev();
    },
    [goNext, goPrev],
  );

  // Keyboard nav for mobile (when mobile layout shown)
  useEffect(() => {
    const onKey = (e: KeyboardEvent) => {
      if (e.key === "ArrowLeft") goPrev();
      if (e.key === "ArrowRight") goNext();
      if (e.key === "Home") goTo(0);
      if (e.key === "End") goTo(N - 1);
    };
    window.addEventListener("keydown", onKey);
    return () => window.removeEventListener("keydown", onKey);
  }, [goPrev, goNext, goTo]);

  return (
    <section className="section-padding" aria-label="Project showcase">
      {/* Heading */}
      <div className="mb-6">
        <p className="section-label mb-2">VENTURE SHOWCASE</p>
        <h2 className="font-display text-5xl leading-none">
          VENTURE <span className="font-accent">Showcase</span>
        </h2>
      </div>

      {/* Counter */}
      <div className="flex items-center justify-between mb-4">
        <span className="text-xs text-muted-foreground tabular-nums font-medium">
          <span className="text-foreground font-bold">
            {String(activeIndex + 1).padStart(2, "0")}
          </span>
          {" / "}
          {String(N).padStart(2, "0")}
        </span>
        <div className="flex gap-2">
          <NavButton
            onClick={goPrev}
            disabled={activeIndex === 0}
            direction="prev"
            label="Previous project"
          />
          <NavButton
            onClick={goNext}
            disabled={activeIndex === N - 1}
            direction="next"
            label="Next project"
          />
        </div>
      </div>

      {/* Swipeable card */}
      <div
        onTouchStart={onTouchStart}
        onTouchEnd={onTouchEnd}
        className="relative min-h-[640px]"
        aria-live="polite"
      >
        <MobileProjectCard
          project={project}
          direction={direction}
          index={activeIndex}
        />
      </div>

      {/* Pagination dots */}
      <div className="mt-5">
        <MobileDots total={N} current={activeIndex} color={project.color} />
      </div>

      {/* Swipe hint — shown only at start */}
      <AnimatePresence>
        {activeIndex === 0 && (
          <motion.p
            initial={{ opacity: 0 }}
            animate={{ opacity: 1, transition: { delay: 1 } }}
            exit={{ opacity: 0 }}
            className="mt-3 text-center text-[10px] uppercase tracking-[0.2em] text-muted-foreground/50"
          >
            Swipe or use arrow keys to navigate
          </motion.p>
        )}
      </AnimatePresence>
    </section>
  );
}

/* ─────────────────────────────────────────────
   DESKTOP SHOWCASE
   
   Architecture — discrete pixel segments (Apple-style):
   ──────────────────────────────────────────────────────
   Wrapper height = N × 100vh
   Each project owns one 100vh segment.
   
   window.scroll listener computes which segment the user
   is in using raw pixel math — no scrollYProgress float.
   
   advanceTo() enforces ±1 step at a time and locks
   further advances for ANIM_LOCK_MS while a transition
   is in flight, preventing project skipping on fast scroll.
   
   Keyboard: ArrowDown/Up, Home/End also trigger advanceTo.
   
   Sticky inner is released naturally when wrapper scrolls
   past — no spacer div, no artificial whitespace.
───────────────────────────────────────────── */
function DesktopShowcase() {
  const wrapperRef = useRef<HTMLDivElement>(null);
  const [activeIndex, setActiveIndex] = useState(0);
  const [direction, setDirection] = useState(1);
  const isAnimating = useRef(false);
  const committedIndex = useRef(0);

  const advanceTo = useCallback((target: number) => {
    const clamped = Math.max(0, Math.min(N - 1, target));
    if (clamped === committedIndex.current) return;
    if (isAnimating.current) return;

    // Only move one step at a time
    const step = clamped > committedIndex.current ? 1 : -1;
    const next = committedIndex.current + step;

    isAnimating.current = true;
    committedIndex.current = next;
    setDirection(step);
    setActiveIndex(next);

    setTimeout(() => {
      isAnimating.current = false;
    }, ANIM_LOCK_MS);
  }, []);

  // Scroll listener — pixel-based segment detection
  useEffect(() => {
    const onScroll = () => {
      const wrapper = wrapperRef.current;
      if (!wrapper) return;
      const wrapperTop = wrapper.getBoundingClientRect().top + window.scrollY;
      const scrolledIn = window.scrollY - wrapperTop;
      const vh = window.innerHeight;
      const rawTarget = Math.floor(scrolledIn / vh);
      advanceTo(rawTarget);
    };

    window.addEventListener("scroll", onScroll, { passive: true });
    return () => window.removeEventListener("scroll", onScroll);
  }, [advanceTo]);

  // Keyboard navigation
  useEffect(() => {
    const onKey = (e: KeyboardEvent) => {
      if (e.key === "ArrowDown" || e.key === "ArrowRight") {
        e.preventDefault();
        advanceTo(committedIndex.current + 1);
      }
      if (e.key === "ArrowUp" || e.key === "ArrowLeft") {
        e.preventDefault();
        advanceTo(committedIndex.current - 1);
      }
      if (e.key === "Home") {
        e.preventDefault();
        advanceTo(0);
      }
      if (e.key === "End") {
        e.preventDefault();
        advanceTo(N - 1);
      }
    };

    window.addEventListener("keydown", onKey);
    return () => window.removeEventListener("keydown", onKey);
  }, [advanceTo]);

  const goPrev = useCallback(
    () => advanceTo(committedIndex.current - 1),
    [advanceTo],
  );
  const goNext = useCallback(
    () => advanceTo(committedIndex.current + 1),
    [advanceTo],
  );

  const project = PROJECTS[activeIndex];

  return (
    <>
      {/* Section heading — normal scroll above sticky zone */}
      <section className="section-padding pb-0">
        <p className="section-label">VENTURE SHOWCASE</p>
        <h2 className="font-display text-6xl md:text-8xl lg:text-[7rem] leading-none mb-16">
          VENTURE <span className="font-accent">Showcase</span>
        </h2>
      </section>

      {/*
       * OUTER WRAPPER — height = N × 100vh
       * Provides scroll budget. Sticky inner pins inside it.
       * Sticky releases automatically when wrapper exits viewport.
       * Zero spacer divs needed.
       */}
      <div
        ref={wrapperRef}
        style={{ height: `${N * 100}vh` }}
        className="relative"
        aria-label="Project showcase — scroll to explore"
      >
        <div
          className="sticky top-0 h-screen w-full overflow-hidden bg-background flex items-stretch"
          role="region"
          aria-live="polite"
          aria-atomic="true"
        >
          {/* Edge fade overlays */}
          <div
            className="pointer-events-none absolute inset-x-0 top-0 h-24 bg-gradient-to-b from-background to-transparent z-20"
            aria-hidden="true"
          />
          <div
            className="pointer-events-none absolute inset-x-0 bottom-0 h-24 bg-gradient-to-t from-background to-transparent z-20"
            aria-hidden="true"
          />

          <div className="flex w-full h-full px-5 md:px-16 lg:px-24 py-10 gap-6 lg:gap-10">
            {/* LEFT: content + nav */}
            <div className="flex-1 flex items-center min-w-0">
              <div className="w-full max-w-sm">
                <ContentPanel
                  project={project}
                  direction={direction}
                  onPrev={goPrev}
                  onNext={goNext}
                  activeIndex={activeIndex}
                />
              </div>
            </div>

            {/* CENTER: timeline */}
            <div className="hidden md:flex w-8 flex-shrink-0">
              <Timeline current={activeIndex} total={N} color={project.color} />
            </div>

            {/* RIGHT: images */}
            <div className="flex-1 relative min-w-0 flex items-center">
              <div className="relative w-full h-[85vh] max-h-[700px]">
                <AnimatePresence custom={direction}>
                  <ImageGroup
                    key={project.id}
                    project={project}
                    direction={direction}
                  />
                </AnimatePresence>
              </div>
            </div>
          </div>

          {/* Scroll hint */}
          <AnimatePresence>
            {activeIndex === 0 && (
              <motion.div
                className="absolute bottom-8 left-1/2 -translate-x-1/2 flex flex-col items-center gap-2 pointer-events-none z-30"
                initial={{ opacity: 0, y: 10 }}
                animate={{
                  opacity: 1,
                  y: 0,
                  transition: { delay: 1.2, duration: 0.6 },
                }}
                exit={{ opacity: 0, transition: { duration: 0.4 } }}
                aria-hidden="true"
              >
                <p className="text-[10px] tracking-[0.3em] uppercase text-muted-foreground">
                  Scroll to explore
                </p>
                <motion.div
                  className="w-px h-8 bg-muted-foreground/40 origin-top"
                  animate={{ scaleY: [0.3, 1, 0.3] }}
                  transition={{
                    duration: 1.6,
                    repeat: Infinity,
                    ease: "easeInOut",
                  }}
                />
              </motion.div>
            )}
          </AnimatePresence>
        </div>
      </div>
      {/* Next section follows immediately — no spacer */}
    </>
  );
}


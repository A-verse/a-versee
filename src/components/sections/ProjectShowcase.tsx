const Timeline = memo(function Timeline({
  current,
  total,
  color,
}: {
  current: number;
  total: number;
  color: string;
}) {
  return (
    <div
      className="flex flex-col items-center gap-3 h-full justify-center py-8"
      aria-hidden="true"
    >
      <div className="relative w-px flex-1 bg-border rounded-full overflow-hidden">
        <motion.div
          className="absolute top-0 left-0 right-0 rounded-full"
          style={{ background: color }}
          animate={{ height: `${((current + 1) / total) * 100}%` }}
          transition={SPRING}
        />
      </div>
      <div className="flex flex-col gap-3">
        {Array.from({ length: total }).map((_, i) => (
          <motion.div
            key={i}
            className="w-1.5 h-1.5 rounded-full"
            animate={{
              backgroundColor: i <= current ? color : "hsl(var(--border))",
              scale: i === current ? 1.4 : 1,
            }}
            transition={{ duration: 0.35 }}
          />
        ))}
      </div>
    </div>
  );
});

